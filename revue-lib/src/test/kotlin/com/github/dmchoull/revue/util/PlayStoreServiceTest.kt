package com.github.dmchoull.revue.util

import android.content.Context
import android.content.Intent
import android.net.Uri
import com.nhaarman.mockitokotlin2.argumentCaptor
import com.nhaarman.mockitokotlin2.doReturn
import com.nhaarman.mockitokotlin2.mock
import com.nhaarman.mockitokotlin2.verify
import org.amshove.kluent.shouldEqual
import org.junit.Test
import org.junit.runner.RunWith
import org.robolectric.RobolectricTestRunner

@RunWith(RobolectricTestRunner::class)
internal class PlayStoreServiceTest {
    @Test
    fun openAppInStore() {
        val context: Context = mock {
            on { packageName } doReturn "com.github.dmchoull.revue"
        }

        PlayStoreService().openAppInStore(context)

        argumentCaptor<Intent>().apply {
            verify(context).startActivity(capture())

            val intent = firstValue
            intent.action shouldEqual Intent.ACTION_VIEW
            intent.data shouldEqual Uri.parse("market://details?id=com.github.dmchoull.revue")
        }
    }
}
